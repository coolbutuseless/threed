

#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
#' Create a vec4 object
#'
#' @param x,y,z,w vector. default c(0, 0, 0, 1)
#'
#' @return vec4
#'
#' @export
#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
vec4 <- function(x = 0, y = 0, z = 0, w = 1) {
  c(x, y, z, w)
}